package com.Rohit.app;

public class Switch_Case {
    public static void main(String[] args) {
        int numOfAngles =20;

        switch (numOfAngles){

            case 3:
                System.out.println("Traingle");
                break;

            case 4:
                System.out.println("Rectangle");
                break;

            case 5:
                System.out.println("Pentagon");
                break;

            case 6:
                System.out.println("Unknown Shape");
                break;
        }
    }

}
